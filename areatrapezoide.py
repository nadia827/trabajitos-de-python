print("cálculo del área de un trapezoide")

#datos necesarios para calcular

altura_trapezoide = float(input("si la altura es:"))
base_inferior = float(input("la base inferior es:"))
base_superior = float(input("la base superior es:"))


area = 0.5*(base_inferior + base_superior)*altura_trapezoide

print("entonces, el área del bendito trapezoide es de", area, "cm^2")

